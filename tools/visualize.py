# 10. Basket Vol
# ============================================================================
def plot_basket_vol():
    df = pd.read_csv("viz_basket_vol.csv")

    fig, ax = plt.subplots(figsize=(12, 7))
    labels = ["ρ=0.0", "ρ=0.3", "ρ=0.5", "ρ=0.8", "ρ=1.0"]
    cols = [c for c in df.columns if c != "N"]

    for i, (col, label) in enumerate(zip(cols, labels)):
        ax.plot(df["N"], df[col] * 100, "o-", color=COLORS[i], label=label, markersize=5)

    # Asymptotic limits
    sigma = 20
    for rho, color in zip([0.0, 0.3, 0.5, 0.8, 1.0], COLORS):
        limit = sigma * np.sqrt(max(rho, 0))
        ax.axhline(limit, color=color, alpha=0.3, linestyle=":")

    ax.set_xlabel("Number of Assets (N)")
    ax.set_ylabel("Basket Volatility (%)")
    ax.set_title("Diversification Effect — Basket Vol vs Number of Assets\n"
                 r"Limit as $N \to \infty$: $\sigma_{basket} \to \sigma\sqrt{\rho}$")
    ax.legend()
    ax.set_ylim(bottom=0)
    save(fig, "11_basket_vol")


