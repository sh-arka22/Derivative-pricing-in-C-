# 3. Monte Carlo Convergence
# ============================================================================
def plot_mc_convergence():
    df = pd.read_csv("viz_mc_convergence.csv")

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 7))

    # Price convergence
    ax1.fill_between(df["NumPaths"], df["ConfLo"], df["ConfHi"],
                     alpha=0.2, color=ACCENT, label="95% CI")
    ax1.plot(df["NumPaths"], df["MCPrice"], "o-", color=ACCENT,
             markersize=3, label="MC Price")
    ax1.axhline(df["AnalyticPrice"].iloc[0], color=ACCENT2,
                linestyle="--", label="Analytic (BS)")
    ax1.set_xscale("log")
    ax1.set_xlabel("Number of Paths")
    ax1.set_ylabel("Price ($)")
    ax1.set_title("MC Price Convergence")
    ax1.legend()

    # Standard error convergence
    ax2.plot(df["NumPaths"], df["StdError"], "o-", color=ACCENT3, markersize=3)
    # Theoretical 1/sqrt(N) line
    N = df["NumPaths"].values.astype(float)
    se0 = df["StdError"].iloc[0] * np.sqrt(N[0])
    ax2.plot(N, se0 / np.sqrt(N), "--", color=ACCENT2,
             label=r"$O(1/\sqrt{N})$ theory", alpha=0.7)
    ax2.set_xscale("log")
    ax2.set_yscale("log")
    ax2.set_xlabel("Number of Paths")
    ax2.set_ylabel("Standard Error")
    ax2.set_title("MC Standard Error (log-log)")
    ax2.legend()

    fig.suptitle("Monte Carlo Convergence — European Call (Antithetic Variates)",
                 fontsize=14, y=1.02)
    plt.tight_layout()
    save(fig, "04_mc_convergence")


