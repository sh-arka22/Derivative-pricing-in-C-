# 1. Volatility Surface
# ============================================================================
def plot_vol_surface():
    df = pd.read_csv("viz_vol_surface.csv")
    strikes = sorted(df["Strike"].unique())
    mats = sorted(df["Maturity"].unique())

    K, T = np.meshgrid(strikes, mats)
    IV = np.full_like(K, np.nan, dtype=float)

    for i, t in enumerate(mats):
        for j, k in enumerate(strikes):
            row = df[(df["Maturity"] == t) & (df["Strike"] == k)]
            if not row.empty:
                IV[i, j] = row["ImpliedVol"].values[0]

    fig = plt.figure(figsize=(14, 9))
    ax = fig.add_subplot(111, projection="3d")
    surf = ax.plot_surface(K, T, IV * 100, cmap=cm.plasma, alpha=0.9,
                           edgecolor="white", linewidth=0.2)
    ax.set_xlabel("Strike", labelpad=10)
    ax.set_ylabel("Maturity (years)", labelpad=10)
    ax.set_zlabel("Implied Vol (%)", labelpad=10)
    ax.set_title("Heston Implied Volatility Surface\n"
                 r"$v_0=0.04,\ \kappa=2,\ \theta=0.04,\ \xi=0.3,\ \rho=-0.7$",
                 fontsize=14, pad=20)
    fig.colorbar(surf, ax=ax, shrink=0.5, label="IV (%)")
    ax.view_init(elev=25, azim=-45)
    save(fig, "01_vol_surface")

    # 2D smile slices
    fig, ax = plt.subplots(figsize=(12, 7))
    for i, t in enumerate(mats):
        subset = df[df["Maturity"] == t].sort_values("Strike")
        if len(subset) > 3:
            ax.plot(subset["Strike"], subset["ImpliedVol"] * 100,
                    "o-", color=COLORS[i % len(COLORS)], label=f"T={t:.2f}y",
                    markersize=5)
    ax.axvline(100, color="white", alpha=0.3, linestyle="--", label="ATM")
    ax.set_xlabel("Strike")
    ax.set_ylabel("Implied Volatility (%)")
    ax.set_title("Heston Volatility Smile — Skew by Maturity")
    ax.legend()
    save(fig, "02_vol_smile")


